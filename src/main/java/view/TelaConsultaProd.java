    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed

        int id = 0, linha = 0, QtdEstoque = 0;
        String produto = "";
        double VlrUnitario = 0.00;

        linha = tblResultados.getSelectedRow();

        id = Integer.parseInt(tblResultados.getValueAt(linha, 0).toString());
        produto = tblResultados.getValueAt(linha, 1).toString();
        VlrUnitario = Double.parseDouble(tblResultados.getValueAt(linha, 2).toString());
        QtdEstoque = Integer.parseInt(tblResultados.getValueAt(linha, 3).toString());

        Produtos objProduto = new Produtos();

        objProduto.setCodigoProduto(id);
        objProduto.setNomeProduto(produto);
        objProduto.setPrecoProduto(VlrUnitario);
        objProduto.setEstoqueProduto(QtdEstoque);

        TelaCadProdutos frame = new TelaCadProdutos(objProduto);
        frame.setVisible(true);
        frame.setLocationRelativeTo(null);


    }//GEN-LAST:event_btnAlterarActionPerformed

