    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        int id = 0, linha = 0, resultado = 0;
        String NomeProd = "";
        boolean retVen, retDel, retIni = false;
        Object[] opcao = { "Sim", "Não" };

        linha = tblResultados.getSelectedRow();

        id = Integer.parseInt(tblResultados.getValueAt(linha, 0).toString());
        NomeProd = tblResultados.getValueAt(linha,1).toString();
        
        retVen = ProdutoDAO.ValidaVendasProduto(id);
        
        if(retVen){
           JOptionPane.showMessageDialog(this, "Não é possível excluir o produto"+
                String.valueOf(id) +" - "+ NomeProd + " pois o mesmo tem Vendas vinculadas ao seu cadastro.\n");
   
        }else{
            resultado = JOptionPane.showOptionDialog(null, "Deseja mesmo excluir o produto "+
                String.valueOf(id) +" - "+ NomeProd + " ?",
                "Deletar Produto",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, opcao, opcao[1]);
            
            if(resultado == 1){
                return;
            }

            retDel = ProdutoDAO.excluir(id);

            if (retDel) {
                JOptionPane.showMessageDialog(this, "Produto excluido com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir Produto.");
            }
            btnConsultar.doClick();
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

