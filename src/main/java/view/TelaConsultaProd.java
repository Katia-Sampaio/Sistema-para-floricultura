    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        int TG_ESTOQUE = 0;

        if(chkSomenteEstoque.isSelected()){
            TG_ESTOQUE = 1;
        }else{
            TG_ESTOQUE = 0;
        }

        if (txtDS_MODELO.getText().trim().equals("") && txtPK_ID.getText().trim().equals("")) {
            ArrayList<Produtos> listaProduto = ProdutoDAO.listar(TG_ESTOQUE);

            DefaultTableModel modelo = (DefaultTableModel) tblResultados.getModel();
            modelo.setRowCount(0);

            for (Produtos Produto : listaProduto) {
                modelo.addRow(new String[]{String.valueOf(Produto.getCodigoProduto()),
                    Produto.getNomeProduto(),
                    String.valueOf(Produto.getPrecoProduto()),
                    String.valueOf(Produto.getEstoqueProduto())
                });
            }
        } else if ((!(txtDS_MODELO.getText().trim().equals("")) && !(txtPK_ID.getText().trim().equals("")))) {

            String NomeProd = txtDS_MODELO.getText();
            int CodProduto = Integer.parseInt(txtPK_ID.getText());

            ArrayList<Produtos> listaProduto = ProdutoDAO.buscarProdutoNomeCodigo(NomeProd, CodProduto, TG_ESTOQUE);

            DefaultTableModel modelo = (DefaultTableModel) tblResultados.getModel();
            modelo.setRowCount(0);

            for (Produtos Produto : listaProduto) {
                modelo.addRow(new String[]{String.valueOf(Produto.getCodigoProduto()),
                    Produto.getNomeProduto(),
                    String.valueOf(Produto.getPrecoProduto()),
                    String.valueOf(Produto.getEstoqueProduto())
                });
            }
        } else if ((!(txtDS_MODELO.getText().trim().equals("")) && (txtPK_ID.getText().trim().equals("")))) {

            String NomeProd = txtDS_MODELO.getText();

            ArrayList<Produtos> listaProduto = ProdutoDAO.buscarProdutoNome(NomeProd, TG_ESTOQUE);

            DefaultTableModel modelo = (DefaultTableModel) tblResultados.getModel();
            modelo.setRowCount(0);

            for (Produtos Produto : listaProduto) {
                modelo.addRow(new String[]{String.valueOf(Produto.getCodigoProduto()),
                    Produto.getNomeProduto(),
                    String.valueOf(Produto.getPrecoProduto()),
                    String.valueOf(Produto.getEstoqueProduto())
                });

            }
        } else if (((txtDS_MODELO.getText().trim().equals("")) && !(txtPK_ID.getText().trim().equals("")))) {

            int CodProduto = Integer.parseInt(txtPK_ID.getText());

            ArrayList<Produtos> listaProduto = ProdutoDAO.buscarProdutoCodigo(CodProduto, TG_ESTOQUE);

            DefaultTableModel modelo = (DefaultTableModel) tblResultados.getModel();
            modelo.setRowCount(0);

            for (Produtos Produto : listaProduto) {
                modelo.addRow(new String[]{String.valueOf(Produto.getCodigoProduto()),
                    Produto.getNomeProduto(),
                    String.valueOf(Produto.getPrecoProduto()),
                    String.valueOf(Produto.getEstoqueProduto())
                });

            }
        }

    }//GEN-LAST:event_btnConsultarActionPerformed

