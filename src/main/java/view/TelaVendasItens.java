    public void totalizarProduto(){
        int IdProduto = 0, QtMovimento = 0;
        Double ValorProduto = 0.00, ValorTotal = 0.00, ValorDesconto = 0.00;
        
        // VALIDAÇÃO DOS DADOS
        if(txtFK_PRODUTO.getText() .isEmpty() || txtQT_MOVIMENTO.getText() .isEmpty() ||
            txtVL_PREUNI.getText() .isEmpty()){
            return;
        }
        
        // ATRIBUIÇÃO DE VALORES
        IdProduto = Integer.parseInt(txtFK_PRODUTO.getText());
        QtMovimento = Integer.parseInt(txtQT_MOVIMENTO.getText());
        ValorProduto = Double.valueOf(txtVL_PREUNI.getText());
       
        ValorTotal = ValorProduto * Double.valueOf(QtMovimento);
        
        if(!txtVL_DESCONTO.getText() .isEmpty()){
            ValorDesconto = Double.valueOf(txtVL_DESCONTO.getText());        
            
            ValorTotal = ValorTotal - (ValorTotal * (ValorDesconto/100.00));
        }
        
        txtVL_PRETOT.setText(String.valueOf(ValorTotal));
    }
