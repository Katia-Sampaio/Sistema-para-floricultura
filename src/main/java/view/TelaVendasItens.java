    private void txtFK_PRODUTOFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtFK_PRODUTOFocusLost
        boolean retorno = false;
        int CodProduto = 0;
        String NomeProd = "";
        Double VlProduto = 0.00;
        
        if(!txtFK_PRODUTO.getText().trim().equals("")){
            CodProduto = Integer.parseInt(txtFK_PRODUTO.getText().trim());
            
            retorno = VendasDAO.ValidaProdutoExistente(CodProduto);
            
            if(!retorno){
                JOptionPane.showMessageDialog(this,"Produto não encontrado.","Erro no Produto",WARNING_MESSAGE);
                txtDS_PRODUTO.setText("");
            }else{
                NomeProd = VendasDAO.RetornaNomeProduto(CodProduto);
                txtDS_PRODUTO.setText(NomeProd);
                
                if((ProdTemp > 0 && CodProduto != ProdTemp) || ProdTemp == 0){
                    VlProduto = VendasDAO.RetornaValorProduto(CodProduto);
                    txtVL_PREUNI.setText(String.valueOf(VlProduto)); 
                }
                               
            }
        }else{
            txtDS_PRODUTO.setText("");
        }   
    }//GEN-LAST:event_txtFK_PRODUTOFocusLost

