    private void btnFINALIZARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFINALIZARActionPerformed
        int IdVenda = 0, IdProduto = 0, QtMovimento = 0, QtEstoque = 0;
        Double ValorProduto = 0.00, ValorTotal = 0.00, ValorDesconto = 0.00;
        boolean retorno = false;
               
        if(txtFK_PRODUTO.getText() .isEmpty() || txtQT_MOVIMENTO.getText() .isEmpty() 
            || txtVL_DESCONTO.getText() .isEmpty() || txtVL_PREUNI.getText() .isEmpty()){
            JOptionPane.showMessageDialog(this, "Campo obrigatório não preenchido.");
            return;
        }
        
        IdProduto = Integer.parseInt(txtFK_PRODUTO.getText().trim());
        
        retorno = VendasDAO.ValidaProdutoExistente(IdProduto);

        if(!retorno){
            JOptionPane.showMessageDialog(this,"Produto não encontrado ou inativo no sistema","Erro no Produto",WARNING_MESSAGE);
            return;
        }
        
        if(Linha < 0){
            int size = VendasCab.listaItens.size();
        
            for (int i = 0; i < size; i++) {
                VendasItem item = VendasCab.listaItens.get(i);
                int Cod = 0;
                if (item != null) {
                    Object[] row = new Object[5];

                    Cod = item.getCodProduto();

                    if(Cod == IdProduto){
                        JOptionPane.showMessageDialog(this,"Produto já está na venda, favor alterar quantidade."
                                ,"Duplicidade de Produto",WARNING_MESSAGE);
                        return;
                    }
                }
            }
        }
        
        QtMovimento = Integer.parseInt(txtQT_MOVIMENTO.getText());
        ValorProduto = Double.valueOf(txtVL_PREUNI.getText());
        ValorTotal = Double.valueOf(txtVL_PRETOT.getText());
        ValorDesconto = Double.valueOf(txtVL_DESCONTO.getText());        
              
        QtEstoque = VendasDAO.ValidaQtEstoque(IdProduto);
        
        
        if(ValorProduto <= 0.00){
            JOptionPane.showMessageDialog(this, "Valor do produto inválido.");
            return; 
        }
        
        if(QtEstoque < QtMovimento){
            JOptionPane.showMessageDialog(this, 
                    "Quantidade insuficiente no estoque!"+
                    "\nProduto: "+String.valueOf(IdProduto) +" - " + txtDS_PRODUTO.getText()+
                    "\nQtd. Solicitada: " + String.valueOf(QtMovimento)+
                    "\nQtd. Disponível: " + String.valueOf(QtEstoque));
            return;
        }
        
        objItem.setIdVenda(IdVenda);
        objItem.setCodProduto(IdProduto);
        objItem.setQtMovimento(QtMovimento);
        objItem.setVlUnitario(ValorProduto);
        objItem.setVlTotal(ValorTotal);
        objItem.setVlDesconto(ValorDesconto);
              
        if(Linha >= 0){
            VendasCab.listaItens.set(Linha, objItem);
        }else{    
            VendasCab.listaItens.add(objItem);
        }
        
        VendasCab.ambientarGrade();
        this.dispose();
    }//GEN-LAST:event_btnFINALIZARActionPerformed

