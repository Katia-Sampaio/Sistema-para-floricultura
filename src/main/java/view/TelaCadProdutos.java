    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        String NomeProd = "";
        Double PrecoUnit = 0.00;
        int QtdEstoque = 0;
        int id = 0;
        boolean retorno;

        if ((txtDS_MODELO.getText().trim().isEmpty()) || (txtVL_PRECO.getText().trim().isEmpty())
                || (txtQT_ESTOQUE.getText().trim().isEmpty())){
            JOptionPane.showMessageDialog(this,"Campo obrigatório não preenchido!");
            return;
        }
        
        NomeProd = txtDS_MODELO.getText();
        PrecoUnit = Double.valueOf(txtVL_PRECO.getText());
        QtdEstoque = Integer.parseInt(txtQT_ESTOQUE.getText());

        if (txtPK_ID.getText().trim().equals("")) {

            Produtos objProduto = new Produtos(NomeProd, PrecoUnit, QtdEstoque);

            retorno = ProdutoDAO.salvar(objProduto);

            if (retorno) {
                JOptionPane.showMessageDialog(this, "Dados incluidos com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao incluir dados.");
            }

        } else {

            id = Integer.parseInt(txtPK_ID.getText());

            Produtos objProduto = new Produtos(id, NomeProd, PrecoUnit, QtdEstoque);

            retorno = ProdutoDAO.alterar(objProduto);

            if (retorno) {
                JOptionPane.showMessageDialog(this, "Produto alterado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao alterar produto.");
            }

        }

        this.dispose();
    }//GEN-LAST:event_btnIncluirActionPerformed


