    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        if (txtDS_MODELO.getText().isEmpty() && txtVL_PRECO.getText().isEmpty() && txtQT_ESTOQUE.getText().isEmpty()) {
            this.dispose();

        } else {
            int resultado = JOptionPane.showConfirmDialog(null, "Tem certeza que seja cancelar?",
                    "Saída", JOptionPane.YES_NO_OPTION);
            if (resultado == JOptionPane.YES_OPTION) {
                this.dispose();
            }
        }

    }//GEN-LAST:event_btnCancelarActionPerformed

    private void txtPK_IDKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPK_IDKeyTyped

        if (txtPK_ID.getText().length() >= 5) {
            evt.consume();

        }

        char c = evt.getKeyChar();
        if (((c < '0') || (c > '9')) && (c != KeyEvent.VK_BACK_SPACE)) {
            evt.consume();

        }


    }//GEN-LAST:event_txtPK_IDKeyTyped

