    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
       
        String NomeProd = txtDS_MODELO.getText();

        ArrayList<Produtos> listaProduto = ProdutoDAO.buscarProdutoNome(NomeProd, 1);

        DefaultTableModel modelo = (DefaultTableModel) tblResultados.getModel();
        modelo.setRowCount(0);

        for (Produtos Produto : listaProduto){
            modelo.addRow(new String[]{String.valueOf(Produto.getCodigoProduto()),
                Produto.getNomeProduto(),
                String.valueOf(Produto.getPrecoProduto()),
                String.valueOf(Produto.getEstoqueProduto())
            });

        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        int linha = -1;
        
        linha =  tblResultados.getSelectedRow();
        
        if(linha < 0){
            return;
        }

        VendasItem.txtFK_PRODUTO.setText(String.valueOf(tblResultados.getValueAt(linha,0)));
        VendasItem.txtDS_PRODUTO.setText(String.valueOf(tblResultados.getValueAt(linha,1)));
        VendasItem.txtVL_PREUNI.setText(String.valueOf(tblResultados.getValueAt(linha,2)));
        VendasItem.totalizarProduto();
        //VendasItem.ProdTemp = Integer.parseInt(tblResultados.getValueAt(linha,0).toString());
        
        this.dispose();
    }//GEN-LAST:event_btnSelecionarActionPerformed

